import random

def глухая_бабушка():
    """
    Программа для разговора с глухой бабушкой.
    
    Бабушка плохо слышит и отвечает "АСЬ?! ГОВОРИ ГРОМЧЕ, ВНУЧЕК!"
    на обычные слова, и "НЕТ, НИ РАЗУ С <ГОД> ГОДА!" на крик 
    (слова заглавными буквами).
    
    Чтобы закончить разговор, нужно три раза подряд прокричать "ПОКА!".
    """
    
    print("ЧЕГО СКАЗАТЬ-ТО ХОТЕЛ, МИЛОК?!")
    
    счетчик_пока = 0  
    
    while True:
       
        пользовательский_ввод = input("> ")
        
       
        if пользовательский_ввод.isupper():
      
            if пользовательский_ввод == "ПОКА!":
                счетчик_пока += 1
                
          
                if счетчик_пока == 3:
                    print("ДО СВИДАНИЯ, МИЛЫЙ!")
                    break
                else:
            
                    случайный_год = random.randint(1930, 1950)
                    print(f"НЕТ, НИ РАЗУ С {случайный_год} ГОДА!")
            else:
           
                счетчик_пока = 0  
                случайный_год = random.randint(1930, 1950)
                print(f"НЕТ, НИ РАЗУ С {случайный_год} ГОДА!")
        else:
         
            счетчик_пока = 0  
            print("АСЬ?! ГОВОРИ ГРОМЧЕ, ВНУЧЕК!")

if __name__ == "__main__":
    глухая_бабушка()